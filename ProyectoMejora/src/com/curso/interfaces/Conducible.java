package com.curso.interfaces;

/**
 * Interfaz que define los metodos necesarios para conducir un vehiculo.
 */
public interface Conducible {

	/**
	 * Metodo para conducir el vehiculo.
	 */
	void conducir();

	/**
	 * Metodo para avanzar el vehiculo.
	 */
	void avanzar();

	/**
	 * Metodo para retroceder el vehiculo.
	 */
	void retroceder();

	/**
	 * Metodo para detener el vehiculo.
	 */
	void parar();
	
	
}
